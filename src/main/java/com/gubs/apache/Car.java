package com.gubs.apache;

/**
 * @author gubs
 *
 */
public class Car {
  /**
     * 
     */
  private int brake;
  private int tires;
  private String owner;

  public int getBrake() {
    return brake;
  }

  public void setBrake(int brake) {
    this.brake = brake;
  }

  public int getTires() {
    return tires;
  }

  public void setTires(int tires) {
    this.tires = tires;
  }

  public String getOwner() {
    return owner;
  }

  public void setOwner(String owner) {
    this.owner = owner;
  }

}
